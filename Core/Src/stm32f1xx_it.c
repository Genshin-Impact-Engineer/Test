/*
 * EXTI15_10_IRQHandler —— 外部中断 15~10 共用处理函数
 * 本系统中用于处理按键 K1~K4（PB12~PB15）的 EXTI 下降沿中断
 *
 * PB12 → EXTI12（K1）  PB13 → EXTI13（K2）
 * PB14 → EXTI14（K3）  PB15 → EXTI15（K4）
 *
 * 每个按键单独调用 HAL_GPIO_EXTI_IRQHandler()，
 * 该函数会清除对应标志位并调用 HAL_GPIO_EXTI_Callback()
 *
 * 注意：CubeMX 配置了 EXTI 中断但未自动生成此处理函数，
 * 需要手动添加，否则按键中断会触发 Default_Handler 导致 HardFault
 */
void EXTI15_10_IRQHandler(void) {
    HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_12);
    HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_13);
    HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_14);
    HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_15);
}

/*
 * HAL_ADC_ConvCpltCallback —— ADC 转换完成回调
 * ADC1 在 DMA 循环模式下连续采集两个通道（温度 + 浊度）
 * 每次转换完成 DMA 传输 → 触发此回调 → 设置 dma_flag 通知主循环
 *
 * dma_flag 在主循环的传感器处理段被消费并清零
 * （确保主循环与中断间的一致性，volatile 修饰）
 */
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc) {
    if (hadc->Instance == ADC1) {
        hsensor.dma_flag = 1;   /* 通知主循环有新的 ADC 数据可用 */
    }
}

